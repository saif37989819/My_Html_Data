ope = input('Enter Operator Function what you want (e.g. +=Add, *=Multiply, //= divide, -=substraction /=floating divide\:')
int2 = int(input('Enter your second Digit \:'))
if ope == '+':
    print('Your Addition Result is  : ', int1 + int2)
